CREATE TABLE T_INSTITUICAO_INVESTIMENTO (
    ID_INSTITUICAO       NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOME_INSTITUICAO     VARCHAR2(150) NOT NULL,
